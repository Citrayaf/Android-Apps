package com.example.afafadhlika.ikan.Model;

public class Dataa {
    String hit;
    int pos;


    public Dataa() {
        this.hit = hit;
        this.pos = pos;
    }

    public Dataa(String hit, int pos) {
        this.hit = hit;
        this.pos = pos;
    }

    public int getPos() {
        return pos;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }

    public String getHit() {
        return hit;
    }

    public void setHit(String hit) {
        this.hit = hit;
    }
}
